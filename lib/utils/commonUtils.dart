import 'package:flutter_screenutil/flutter_screenutil.dart';

class CommonUtils {
  static double adaptHeight(double height) {
    return height.h;
  }

  static double adaptWidth(double width) {
    return width.h;
  }

  static double adaptFontSize(double fontSize) {
    return fontSize.sp;
  }
}
